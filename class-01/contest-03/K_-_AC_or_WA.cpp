#include <bits/stdc++.h>
using namespace std;

#define endn '\n'

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    cin >> n >> m;

    if (n == m)
    {
        cout << "Yes" << endn;
    }
    else
    {
        cout << "No" << endn;
    }

    return 0;
}
