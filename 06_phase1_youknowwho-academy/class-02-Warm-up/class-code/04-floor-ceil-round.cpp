#include <bits/stdc++.h>
using namespace std;

#define endn "\n"

int32_t main()
{
    double d = 2.6;
    cout << floor(d) << endn;
    cout << ceil(d) << endn;
    cout << round(d) << endn;

    cout << "round(2.8): " << round(2.8) << endn;
    cout << "round(2.5): " << round(2.5) << endn;
    cout << "round(2.49): " << round(2.49) << endn;
    cout << "round(2.4): " << round(2.4) << endn;
}
